/**
 * Config Layer — Application Configuration Classes.
 *
 * This package contains Java-based configuration classes that customize
 * how Spring Boot components behave:
 *
 *   - CorsConfig:    Configures Cross-Origin Resource Sharing so the React frontend
 *                    (running on port 3000) can communicate with the backend (port 8080).
 *
 *   - OpenApiConfig: Customizes Swagger/OpenAPI documentation with project metadata,
 *                    security scheme definitions, and API grouping.
 *
 *   - WebConfig:     General web configuration (resource handlers, interceptors).
 */
package com.smarthire.config;
